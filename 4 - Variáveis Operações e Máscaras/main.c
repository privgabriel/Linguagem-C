#include <stdio.h> //Biblioteca padrão de entrada e saída
#include <stdlib.h> //Biblioteca padrão de funções

int main() {
    int primeiraVariavel = 15; //Declaração de variável
    int segundaVariavel = 4; //Declaração de variável
    primeiraVariavel += segundaVariavel; //Soma a primeira variável com a segunda variável

    int resultado; //Declaração de variável

    printf("Primeira variável: %d\n", primeiraVariavel); //%d é um placeholder para um número inteiro
    printf("Segunda variável: %d\n", segundaVariavel); //%d é um placeholder para um número inteiro
    printf("Soma: %d\n", primeiraVariavel); //%d é um placeholder para um número inteiro
    printf("Resultado: %d\n", resultado); //%d é um placeholder para um número inteiro
}